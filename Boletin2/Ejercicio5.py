"""
Escribir un programa que lea el valor de una distancia en millas marinas y la pase a
metros (1 milla marina = 1852 m).
"""
millasmarinas = float(input("Dime un valor de la distancia en millas marinas:"))

calculo = millasmarinas * 1852

print("El calculo de la distancia en metros es:", calculo)